# 시키고 싶은 일: 숫자, 글자 데이터를 화면에 출력하기
# 출력하기 => print( 데이터 )
# 숫자 데이터 출력 => 정수 int
print(2024)
print(1227)
print(33)
print(1000)

# 숫자 데이터 출력 => 실수 float
print(2.9)
print(3.14)
print(1.)

# 문자/글자/문자열 데이터 출력 => 글자 str
# '데이터', "데이터"
print("홍길동")
print("winter")
print("2024.")

# '''데이터''', """데이터""" : 여러줄 문자열
print('''
      Good
      LUCK         
      HAPPY''')
